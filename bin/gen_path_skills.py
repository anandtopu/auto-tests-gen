#!/usr/bin/env python3
"""Generate path-triggered OpenHands skills for the UI/API convention split.

The estate AGENTS.md hands every phase everything, so an agent writing an API suite
also receives UI page-object conventions and vice versa. OpenHands supports
*path-triggered* skills: frontmatter `paths:` globs, injected only when the agent
actually reads or edits a matching file (once per conversation, appended to the
tool result). That is a much better fit for a per-discipline split.

The globs are DERIVED FROM THE REGISTRY — each test repo already declares its
`layer` (api|ui) and `layout` (specs/fixtures/pages dirs), so adding a repo or
changing a layout keeps the triggers correct without anyone hand-editing globs.
The convention text itself stays single-sourced in skills/e2e-<layer>-conventions/;
this only wraps it with triggers.

Output: .agents/skills/e2e-<layer>-conventions/SKILL.md  (AUTO-GENERATED)
Regenerated by: make skills, bin/repos.py edits, and repo_admin mutations.

Note: OpenHands path triggers do NOT fire in ACP-backed conversations (the ACP
server owns tool execution). AGENTS.md remains the always-on fallback, so nothing
is lost there — the split is an enhancement, not the only delivery path.
"""
import pathlib, sys

sys.stdout.reconfigure(encoding="utf-8")
ROOT = pathlib.Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "engine/lib"))
from registry import load_registry

OUT_DIR = ROOT / ".agents/skills"
SRC_DIR = ROOT / "skills"
LAYERS = ("api", "ui")


def _body(layer):
    """The canonical convention text, minus its own frontmatter."""
    p = SRC_DIR / f"e2e-{layer}-conventions/SKILL.md"
    if not p.exists():
        return None
    text = p.read_text(encoding="utf-8")
    if text.startswith("---"):
        parts = text.split("---", 2)
        if len(parts) == 3:
            return parts[2].lstrip("\n")
    return text


def globs_for(layer, reg=None):
    """Path globs that mean 'the agent is working on <layer> tests right now'.

    Emitted in two forms because the agent's workspace root differs by trigger
    path: the control-repo layout (workspace/tests/<repo>/...) and the
    repo-relative layout (when a test repo itself is the workspace).
    Gitignore-style, POSIX, case-sensitive — '**' crosses '/'."""
    reg = reg or load_registry()
    out = []
    for t in reg["test_repositories"]:
        if t.get("layer") != layer:
            continue
        layout = t.get("layout", {})
        specs = (layout.get("specs") or "").strip("/")
        others = sorted({d.strip("/") for k, d in layout.items()
                         if d and k != "specs"})
        # Inside the control repo the path is unambiguous — match everything.
        for d in filter(None, [specs, *others]):
            out.append(f"workspace/tests/{t['name']}/{d}/**")
        # Repo-relative (a test repo IS the workspace) needs care: a bare
        # "tests/**" would also match this control repo's own tests/ directory and
        # inject UI conventions when an agent touches the gate harness. Scope the
        # specs dir to actual spec files — which is the truer signal anyway.
        for ext in ("js", "ts"):
            if specs:
                out.append(f"{specs}/**/*.spec.{ext}")
        for d in others:
            out.append(f"{d}/**")
    # de-duplicate, keep deterministic order so regeneration is a no-op diff
    return sorted(set(out))


def render(layer, reg=None):
    body = _body(layer)
    if body is None:
        return None
    paths = globs_for(layer, reg)
    if not paths:
        return None
    kind = "API" if layer == "api" else "UI"
    lines = ["---",
             f"name: e2e-{layer}-conventions",
             f"description: {kind} E2E authoring conventions. Injected when the agent "
             f"touches {kind} test files, so API work never receives UI page-object "
             f"rules and vice versa.",
             "paths:"]
    lines += [f'  - "{g}"' for g in paths]
    lines += ["---", "",
              f"<!-- AUTO-GENERATED by bin/gen_path_skills.py — DO NOT EDIT BY HAND.",
              f"     Conventions live in skills/e2e-{layer}-conventions/SKILL.md;",
              f"     the path globs are derived from registry test_repositories. -->",
              ""]
    return "\n".join(lines) + body.rstrip() + "\n"


def generate():
    reg = load_registry()
    written = []
    for layer in LAYERS:
        content = render(layer, reg)
        if content is None:
            continue
        d = OUT_DIR / f"e2e-{layer}-conventions"
        d.mkdir(parents=True, exist_ok=True)
        (d / "SKILL.md").write_text(content, encoding="utf-8", newline="\n")
        written.append({"layer": layer, "path": (d / "SKILL.md").relative_to(ROOT).as_posix(),
                        "globs": len(globs_for(layer, reg))})
    return written


if __name__ == "__main__":
    for w in generate():
        print(f"{w['path']}  ({w['globs']} path trigger(s) for the {w['layer']} layer)")
